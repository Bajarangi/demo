package com.EASiCabs.DataObjects;

public class EmployeeDataObject {
	
	private String employeeName;
	private String employeeId;
	private String employeeEmail;
	private String employeeAddress;
	private String employeeContact;
	private String employeeOfficeLocation;
	private String employeeEmergencyContact;
	private String employeeShift;	
	
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeEmail() {
		return employeeEmail;
	}
	public void setEmployeeEmail(String employeeEmail) {
		this.employeeEmail = employeeEmail;
	}
	public String getEmployeeAddress() {
		return employeeAddress;
	}
	public void setEmployeeAddress(String employeeAddress) {
		this.employeeAddress = employeeAddress;
	}
	public String getEmployeeContact() {
		return employeeContact;
	}
	public void setEmployeeContact(String employeeContact) {
		this.employeeContact = employeeContact;
	}
	public String getEmployeeOfficeLocation() {
		return employeeOfficeLocation;
	}
	public void setEmployeeOfficeLocation(String employeeOfficeLocation) {
		this.employeeOfficeLocation = employeeOfficeLocation;
	}
	public String getEmployeeEmergencyContact() {
		return employeeEmergencyContact;
	}
	public void setEmployeeEmergencyContact(String employeeEmergencyContact) {
		this.employeeEmergencyContact = employeeEmergencyContact;
	}
	public String getEmployeeShift() {
		return employeeShift;
	}
	public void setEmployeeShift(String employeeShift) {
		this.employeeShift = employeeShift;
	}
}
